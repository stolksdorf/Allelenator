def ProcessGene(processedGenes, cString):
	gene = Gene(cString)
	geneFound = False
	
	result = []
	
	for pGene in processedGenes:
		if(pGene.sequence== gene.sequence):
			
			for name in gene.names:
				logging.error(name)
				pGene.names.append(name)
			geneFound = True
		result.append(pGene)

	if(not geneFound):
		result.append(gene)
	
	return result
	
		
