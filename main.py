class Gene():
	#parse a raw string from a file into a gene class
	def __init__(self, rawString):
		self.sequence=''
		self.names=[]
		self.delinquent=False
		self.recessive=False;
		
		temp = rawString.split(" ",1)
		logging.info(temp[0])
		self.names.append(temp[0])
		
		if("r" in temp[1] or "re" in temp[1]):
			self.recessive = True
			temp[1].replace("r", "")
			temp[1].replace("e","")
		
		self.sequence = temp[1].strip()
		if("n" in self.sequence):
			self.delinquent = True
		
		index = 1
		result = ""
		for code in self.sequence :
			if(code == "G" or code =="A" or code == "T" or code == "C" or code == "n"):
				if(index % 10 == 0): result += " "
				result += code
				index += 1
		self.sequence = result
		
